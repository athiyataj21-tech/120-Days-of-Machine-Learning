# ==========================================
# DAY 1: VARIABLES & DATA TYPES 🧪
# ==========================================

# 1. STRING
name = "Athiya Taj"
print("Name:", name)
print("Type:", type(name))

# 2. INTEGER
age = 24
print("\nAge:", age)
print("Type:", type(age))

# 3. FLOAT
cgpa = 8.4
print("\nCGPA:", cgpa)
print("Type:", type(cgpa))

# 4. BOOLEAN
is_graduate = True
print("\nGraduate:", is_graduate)
print("Type:", type(is_graduate))

# ==========================================
# TYPE CONVERSION
# ==========================================

age_float = float(age)
print("\nInteger to Float:", age_float)

cgpa_int = int(cgpa)
print("Float to Integer:", cgpa_int)

age_str = str(age)
print("Integer to String:", age_str)

